package exercises.chapter11;

public class Date {
    int year;
    int month;
    int day;

    public Date() {
        year = 1410;
        month = 7;
        day = 15;
    }

    public Date(int year, int month, int day) {
        this.year = year;
        this.month = month;
        this.day = day;
    }
}
